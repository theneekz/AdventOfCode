const { getInputArray, print } = require("../../utils");
const start = Date.now();

function main() {
  const input = getInputArray(__dirname, "/input.txt").map((l) => l.split(""));
  const allChars = [...new Set(input.flat(1).filter((x) => x != "."))];
  let allAntiNodes = {};
  for (const char of allChars) {
    allAntiNodes = { ...allAntiNodes, ...getAntinodesForChar(char, input) };
  }

  return Object.keys(allAntiNodes).length;
}

const getAntinodesForChar = (char, input) => {
  const antennas = getCoordsForChar(char, input);
  let antiNodes = {};
  for (let i = 0; i < antennas.length - 1; i++) {
    for (let j = i + 1; j < antennas.length; j++) {
      const antinodes = getAntinodesForPair(antennas[i], antennas[j], input);
      for (const coord of antinodes) {
        antiNodes[coord] = true;
      }
    }
  }
  return antiNodes;
};

const getCoordsForChar = (char, input) => {
  let coords = [];
  for (let row = 0; row < input.length; row++) {
    for (let col = 0; col < input[row].length; col++) {
      if (input[row][col] === char) {
        coords.push([row, col]);
      }
    }
  }
  return coords;
};

const getAntinodesForPair = (coordA, coordB, input) => {
  let antinodes = [];
  const rowDiff = coordA[0] - coordB[0];
  const colDiff = coordA[1] - coordB[1];

  const antiA = [coordA[0] + rowDiff, coordA[1] + colDiff];
  try {
    if (input[antiA[0]][antiA[1]]) {
      antinodes.push(antiA);
    }
  } catch (e) {} // out of map bounds
  const antiB = [coordB[0] - rowDiff, coordB[1] - colDiff];
  try {
    if (input[antiB[0]][antiB[1]]) {
      antinodes.push(antiB);
    }
  } catch (e) {} // out of map bounds

  return antinodes;
};

console.log(main());

